#include <unistd.h>

void	print(int a, int b, int c)
{
	c = '0' + (a / 10);
	write(1, &c, 1);
	c = '0' + (a % 10);
	write(1, &c, 1);
	write(1, " ", 1);
	c = '0' + (b / 10);
	write(1, &c, 1);
	c = '0' + (b % 10);
	write(1, &c, 1);
	if (a != 98)
		write(1, &", ", 2);
}

void	ft_print_comb2(void)
{
	int	a;
	int	b;
	int	c;

	a = 0;
	b = 0;
	c = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			print(a, b, c);
			b++;
		}
		a++;
	}
}
